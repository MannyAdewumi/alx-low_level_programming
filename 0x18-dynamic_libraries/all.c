/**
 * mod - function to get remainder of two integers
 * @x: first integer
 * @y: second integer
 * Return: remainder of integers
 */

int mod(int x, int y)
{
	return (x % y);
}
/**
 * mul - function to multiply two integers
 * @x: first integer
 * @y: second integer
 * Return: multiplied integers
 */
int mul(int x, int y)
{
	return (x * y);
}
/**
 * div - function to divide two integers
 * @x: first integer
 * @y: second integer
 * Return: sum of integers
 */

int div(int x, int y)
{
	return (x / y);
}
/**
 * add - function to add two integers
 * @x: first integer
 * @y: second integer
 * Return: sum of integers
 */

int add(int x, int y)
{
	return (x + y);
}
/**
 * sub - function to subtract two integers
 * @x: first integer
 * @y: second integer
 * Return: sum of integers
 */

int sub(int x, int y)
{
	return (x - y);
}
